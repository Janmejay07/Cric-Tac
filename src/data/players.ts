export function suggestPlayers(prefix: string, maxResults: number = 10): string[] {
    const normalized = (prefix || "").trim().toLowerCase();
    if (!normalized) return [];

    const matchesPrefix = (name: string) => {
        const lower = name.toLowerCase();
        if (lower.startsWith(normalized)) return true;
        const parts = lower.split(/\s+/);
        return parts.some((part) => part.startsWith(normalized));
    };

    return players.filter(matchesPrefix).slice(0, maxResults);
}