// Ensure your 'players' array is deduplicated
const uniquePlayers: string[] = Array.from(new Set(players));

export function suggestPlayers(prefix: string, maxResults: number = 10): string[] {
  const normalized = (prefix || "").trim().toLowerCase();
  if (!normalized) return [];

  const matchesPrefixOrAnywhere = (name: string) => {
    const lower = name.toLowerCase();
    // Match if prefix matches start of any word OR appears anywhere
    return lower.includes(normalized);
  };

  // Filter, sort by relevance (names starting with prefix appear first), and limit results
  return uniquePlayers
    .filter(matchesPrefixOrAnywhere)
    .sort((a, b) => {
      const aLower = a.toLowerCase();
      const bLower = b.toLowerCase();
      const aStarts = aLower.startsWith(normalized) ? 0 : 1;
      const bStarts = bLower.startsWith(normalized) ? 0 : 1;
      return aStarts - bStarts; // prioritize names starting with the prefix
    })
    .slice(0, maxResults);
}
